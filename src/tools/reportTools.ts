// 3. formatForTeams
// ---------------------------------------------------------------------------

/**
 * Convert markdown tables (which Teams bot messages do NOT render) into
 * readable bold-label lines.
 *
 * Input:
 *   | Category | Budget | Actual |
 *   |----------|--------|--------|
 *   | Revenue  | $4.94M | $4.88M |
 *
 * Output:
 *   **Revenue** · Budget: $4.94M · Actual: $4.88M
 */
function convertMarkdownTables(md: string): string {
  return md.replace(
    /^\|(.+)\|\r?\n\|[-|: ]+\|\r?\n((?:^\|.+\|\r?\n?)*)/gm,
    (fullMatch) => {
      const lines = fullMatch.trim().split(/\r?\n/).filter(Boolean);
      if (lines.length < 3) return fullMatch; // not a real table

      const headers = lines[0].split('|').filter(c => c.trim()).map(c => c.trim());
      const dataRows = lines.slice(2); // skip header + separator

      const converted = dataRows.map(line => {
        const cells = line.split('|').filter(c => c.trim()).map(c => c.trim());
        if (headers.length === 2) {
          // Simple key–value table → "**Key**: Value"
          return `**${cells[0] ?? ''}**: ${cells[1] ?? ''}`;
        }
        // Multi-column: first cell bold, rest as "Header: value" pairs
        return cells.map((cell, i) =>
          i === 0 ? `**${cell}**` : `${headers[i] ?? i}: ${cell}`
        ).join(' · ');
      });

      return converted.join('\n') + '\n';
    },
  );
}

export function formatForTeams(params: {
  content: string;
  message_type: 'alert' | 'report' | 'info';
}): string {
  const typeHeader: Record<string, string> = {
    alert:  '🚨 **FINANCE ALERT**',
    report: '📊 **FINANCE REPORT**',
    info:   'ℹ️ **FINANCE UPDATE**',
  };

  const header = typeHeader[params.message_type] ?? typeHeader.info;

  // Teams-compatible transformations:
  //   • Flatten headings (#, ##, ###) → bold text
  //   • Replace HR dashes with a unicode line
  //   • Convert markdown tables → readable label lines (Teams ignores table syntax)
  const trimmed = params.content
    .replace(/^#{1,6}\s+/gm, '**')
    .replace(/---+/g, '─────────────────────────')
    .slice(0, 20_000);

  const formatted = convertMarkdownTables(trimmed);

  return [
    header,
    '',
    formatted,
    '',
    `─────────────────────────`,
    `*Sent by Morgan · ${new Date().toUTCString()}*`,
  ].join('\n');
}

